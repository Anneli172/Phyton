import turtle

kylje_pikkus = 100
nurk = 120
varv = "red"


turtle.speed(0)
turtle.color(varv)
#kolmnurk
turtle.forward(kylje_pikkus) 
turtle.left(nurk)
turtle.forward(kylje_pikkus) 
turtle.left(nurk)
turtle.forward(kylje_pikkus) 
turtle.left(nurk)

turtle.penup
turtle.goto(kylje_pikkus*1,5.0)
turtle.pendown()

#kolmnurk 2
turtle.forward(kylje_pikkus) 
turtle.left(nurk)
turtle.forward(kylje_pikkus) 
turtle.left(nurk)
turtle.forward(kylje_pikkus) 
turtle.left(nurk)

turtle.penup
turtle.goto(kylje_pikkus*3,0)
turtle.pendown()
#kolmnurk 3
turtle.forward(kylje_pikkus) 
turtle.left(nurk)
turtle.forward(kylje_pikkus) 
turtle.left(nurk)
turtle.forward(kylje_pikkus) 
turtle.left(nurk)

turtle.done()